import tkinter as tk

# Create the main window
root = tk.Tk()
root.title("Favorite Saying")

# Set the dimensions of the window
root.geometry("400x200")

# Add a label to display the favorite saying
label = tk.Label(
    root,
    text="Murphy's Law.",
    font=("Arial", 14),
    wraplength=350,
    justify="center"
)
label.pack(expand=True)

# Start the main loop
root.mainloop()
